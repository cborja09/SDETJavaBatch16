package class7;

public class E2WhileLoop {
    public static void main(String[] args) {

        int number=0;
        // below we are using the increment operator and increases value by one
        number++; //0 ->1
        number++; //1->2
        System.out.println(number);
        int number2=10;
        number2--;
        System.out.println(number2);
    }
}
