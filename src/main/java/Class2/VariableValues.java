package Class2;

public class VariableValues{
    public static void main(String[] args) {
        byte age=37;
        age=38;
        System.out.println(age);
        short numberOfShoes=10;
        numberOfShoes=11;
        System.out.println(numberOfShoes);
        int yearlySalary=100000;
        yearlySalary=120000;
        System.out.println(yearlySalary);
        long distanceToGym=6;
        distanceToGym=7;
        System.out.println(distanceToGym);
        float priceOfGym=22.99F;
        priceOfGym=23.99F;
        System.out.println(priceOfGym);
        double healthyFood=19.99;
        healthyFood=30.99;
        System.out.println(healthyFood);
        char symbol='!';
        symbol='?';
        System.out.println(symbol);
        boolean isItRaining=true;
        isItRaining=false;
        System.out.println(isItRaining);



    }
}
